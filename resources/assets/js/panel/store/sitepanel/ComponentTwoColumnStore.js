const state = {
    twoColumnTable: "",
    selectTwoColumn: []
};
const getters = {};
const mutations = {
    twoColumnTable(state, twoColumnTable) {
        state.twoColumnTable = twoColumnTable;
    },
    selectTwoColumn(state, selectTwoColumn) {
        state.selectTwoColumn = selectTwoColumn;
    }
};
const actions = {
    twoColumnTable(context, data) {
        const form = new FormData();
        form.append("search", data.searchName);
        form.append("searchColumn", data.searchColumn);
        form.append("order", data.order);
        form.append("name", data.name);
        form.append("startDate", data.startDate);
        form.append("endDate", data.endDate);
        axios.defaults.headers.common["Authorization"] =
            "Bearer " +
            (document.cookie.match("(^|;) ?token=([^;]*)(;|$)")
                ? document.cookie.match("(^|;) ?token=([^;]*)(;|$)")[2]
                : null);
        return new Promise((resolve, reject) => {
            axios
                .post("api/v1/twoColumnTable" + "?page=" + data.pageNum, form)
                .then(response => {
                    const twoColumnTable = response.data;
                    context.commit("twoColumnTable", twoColumnTable);
                    resolve(resolve); //
                })
                .catch(error => {
                    return {
                        type: "error",
                        title: "عملیات با خطلا 1501",
                        text: "لطفا با پشتیبانی ارتباط برقرار کنید"
                    };
                    reject(error);
                });
        });
    },

    selectTwoColumn(context, id) {
        axios.defaults.headers.common["Authorization"] =
            "Bearer " +
            (document.cookie.match("(^|;) ?token=([^;]*)(;|$)")
                ? document.cookie.match("(^|;) ?token=([^;]*)(;|$)")[2]
                : null);
        return new Promise((resolve, reject) => {
            axios
                .get("api/v1/selectTwoColumn/" + id)
                .then(response => {
                    const selectTwoColumn = response.data;
                    context.commit("selectTwoColumn", selectTwoColumn);
                    resolve(resolve); //
                })
                .catch(error => {
                    return {
                        type: "error",
                        title: "عملیات با خطلا 1501",
                        text: "لطفا با پشتیبانی ارتباط برقرار کنید"
                    };
                    reject(error);
                });
        });
    }
};
export default {
    state,
    mutations,
    actions,
    getters
};
