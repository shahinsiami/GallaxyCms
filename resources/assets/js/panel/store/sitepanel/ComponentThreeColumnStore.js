const state = {
    threeColumnTable: "",
    selectThreeColumn: []
};
const getters = {};
const mutations = {
    threeColumnTable(state, threeColumnTable) {
        state.threeColumnTable = threeColumnTable;
    },
    selectThreeColumn(state, selectThreeColumn) {
        state.selectThreeColumn = selectThreeColumn;
    }
};
const actions = {
    threeColumnTable(context, data) {
        const form = new FormData();
        form.append("search", data.searchName);
        form.append("searchColumn", data.searchColumn);
        form.append("order", data.order);
        form.append("name", data.name);
        form.append("startDate", data.startDate);
        form.append("endDate", data.endDate);
        axios.defaults.headers.common["Authorization"] =
            "Bearer " +
            (document.cookie.match("(^|;) ?token=([^;]*)(;|$)")
                ? document.cookie.match("(^|;) ?token=([^;]*)(;|$)")[2]
                : null);
        return new Promise((resolve, reject) => {
            axios
                .post("api/v1/threeColumnTable" + "?page=" + data.pageNum, form)
                .then(response => {
                    const threeColumnTable = response.data;
                    context.commit("threeColumnTable", threeColumnTable);
                    resolve(resolve); //
                })
                .catch(error => {
                    return {
                        type: "error",
                        title: "عملیات با خطلا 1501",
                        text: "لطفا با پشتیبانی ارتباط برقرار کنید"
                    };
                    reject(error);
                });
        });
    },

    selectThreeColumn(context, id) {
        axios.defaults.headers.common["Authorization"] =
            "Bearer " +
            (document.cookie.match("(^|;) ?token=([^;]*)(;|$)")
                ? document.cookie.match("(^|;) ?token=([^;]*)(;|$)")[2]
                : null);
        return new Promise((resolve, reject) => {
            axios
                .get("api/v1/selectThreeColumn/" + id)
                .then(response => {
                    const selectThreeColumn = response.data;
                    context.commit("selectThreeColumn", selectThreeColumn);
                    resolve(resolve); //
                })
                .catch(error => {
                    return {
                        type: "error",
                        title: "عملیات با خطلا 1501",
                        text: "لطفا با پشتیبانی ارتباط برقرار کنید"
                    };
                    reject(error);
                });
        });
    }
};
export default {
    state,
    mutations,
    actions,
    getters
};
