const state = {
    fourColumnTable: "",
    selectFourColumn: []
};
const getters = {};
const mutations = {
    fourColumnTable(state, fourColumnTable) {
        state.fourColumnTable = fourColumnTable;
    },
    selectFourColumn(state, selectFourColumn) {
        state.selectFourColumn = selectFourColumn;
    }
};
const actions = {
    fourColumnTable(context, data) {
        const form = new FormData();
        form.append("search", data.searchName);
        form.append("searchColumn", data.searchColumn);
        form.append("order", data.order);
        form.append("name", data.name);
        form.append("startDate", data.startDate);
        form.append("endDate", data.endDate);
        axios.defaults.headers.common["Authorization"] =
            "Bearer " +
            (document.cookie.match("(^|;) ?token=([^;]*)(;|$)")
                ? document.cookie.match("(^|;) ?token=([^;]*)(;|$)")[2]
                : null);
        return new Promise((resolve, reject) => {
            axios
                .post("api/v1/fourColumnTable" + "?page=" + data.pageNum, form)
                .then(response => {
                    const fourColumnTable = response.data;
                    context.commit("fourColumnTable", fourColumnTable);
                    resolve(resolve); //
                })
                .catch(error => {
                    return {
                        type: "error",
                        title: "عملیات با خطلا 1501",
                        text: "لطفا با پشتیبانی ارتباط برقرار کنید"
                    };
                    reject(error);
                });
        });
    },

    selectFourColumn(context, id) {
        axios.defaults.headers.common["Authorization"] =
            "Bearer " +
            (document.cookie.match("(^|;) ?token=([^;]*)(;|$)")
                ? document.cookie.match("(^|;) ?token=([^;]*)(;|$)")[2]
                : null);
        return new Promise((resolve, reject) => {
            axios
                .get("api/v1/selectFourColumn/" + id)
                .then(response => {
                    const selectFourColumn = response.data;
                    context.commit("selectFourColumn", selectFourColumn);
                    resolve(resolve); //
                })
                .catch(error => {
                    return {
                        type: "error",
                        title: "عملیات با خطلا 1501",
                        text: "لطفا با پشتیبانی ارتباط برقرار کنید"
                    };
                    reject(error);
                });
        });
    }
};
export default {
    state,
    mutations,
    actions,
    getters
};
